const groupInputScore = groupInput.map((group, index) => {
  for (let i = 0; i < group[0].length; i++) {
    if (group[1].includes(group[0][i]) && group[2].includes(group[0][i])) {
      return getScore(group[0][i]);
    }
  }
});

